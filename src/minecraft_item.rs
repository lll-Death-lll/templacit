#[derive(Debug)]
pub struct MinecraftItem<'i> {
    id: &'i str,
}

impl<'i> MinecraftItem<'i> {
    pub fn new(id: &'i str) -> Result<MinecraftItem<'i>, String> {
        if !MINECRAFT_ITEMS.contains(id) {
            return Err("No item with such id".to_owned());
        }

        Ok(MinecraftItem { id })
    }
    pub fn id(&self) -> &'i str {
        self.id
    }
}
